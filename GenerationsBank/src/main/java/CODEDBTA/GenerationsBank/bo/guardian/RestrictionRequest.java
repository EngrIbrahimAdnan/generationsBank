package CODEDBTA.GenerationsBank.bo.guardian;

public class RestrictionRequest {
    private String restrictionStart;
    private String restrictionEnd;

    public String getRestrictionStart() {
        return restrictionStart;
    }

    public void setRestrictionStart(String restrictionStart) {
        this.restrictionStart = restrictionStart;
    }

    public String getRestrictionEnd() {
        return restrictionEnd;
    }

    public void setRestrictionEnd(String restrictionEnd) {
        this.restrictionEnd = restrictionEnd;
    }
}
